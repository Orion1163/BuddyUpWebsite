    <h2 class="h2">How to install XAMPP server on windows</h2>
    <p>We will learn how to install the XAMPP server on windows platform step by step. Follow the below steps and install the XAMPP server on your system.</p>
    <p><strong>Step 1:</strong> Click on the above link provided to download the <strong>XAMPP server</strong> according to your window requirement.</p>
    <img src="images/phpimg/xampp1.JPG" alt="Install PHP"/><br>
    <p><strong>Step 2:</strong> After downloading XAMPP, double click on the downloaded file and allow XAMPP to make changes in your system. A window will pop-up, where you have to click on the <strong>Next</strong> button.</p>
    <img src="images/phpimg/xampp2.jpg" alt="Install PHP"/><br>
    <p><strong>Step 3:</strong> Here, select the components, which you want to install and click <strong>Next</strong>.</p>
    <img src="images/phpimg/xampp3.jpg" alt="Install PHP" /><br>
    <p><strong>Step 4:</strong> Choose a folder where you want to install the XAMPP in your system and click <strong>Next</strong>.</p>
    <img src="images/phpimg/xampp4.jpg" alt="Install PHP" /><br>
    <p><strong>Step 5:</strong> Click <strong>Next</strong> and move ahead.</p>
    <img src="images/phpimg/xampp5.jpg" alt="Install PHP" /><br>
    <p><strong>Step 6:</strong> XAMPP is ready to install, so click on the <strong>Next</strong> button and install the XAMPP.</p>
    <img src="images/phpimg/xampp6.jpg" alt="Install PHP" /><br>
    <p><strong>Step 7:</strong> A finish window will display after successful installation. Click on the <strong>Finish</strong> button. </p>
    <img src="images/phpimg/xampp7.jpg" alt="Install PHP" /><br>
    <p><strong>Step 8:</strong> Choose your preferred language.</p>
    <img src="images/phpimg/xampp8.jpg" alt="Install PHP" /><br>
    <p><strong>Step 9:</strong> XAMPP is ready to use. Start the Apache server and MySQL and run the php program on the localhost. </p>
    <p>How to run PHP programs on XAMPP, see in the next tutorial.</p>
    <img src="images/phpimg/xampp9.jpg" alt="Install PHP" /><br>
