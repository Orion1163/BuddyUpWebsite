    <h1 class="h1">Install PHP</h1>
    <p>To install PHP, we will suggest you to install AMP (Apache, MySQL, PHP) software stack. It is available for all operating systems. There are many AMP options available in the market that are given below:</p>
    <ul class="points">
    <li><strong>WAMP</strong> for Windows</li>
    <li><strong>LAMP</strong> for Linux</li>
    <li><strong>MAMP</strong> for Mac</li>
    <li><strong>SAMP</strong> for Solaris</li>
    <li><strong>FAMP</strong> for FreeBSD</li>
    <li><strong>XAMPP</strong> (Cross, Apache, MySQL, PHP, Perl) for Cross Platform: It includes some other components too such as FileZilla, OpenSSL, Webalizer, Mercury Mail, etc.</li>
    </ul>
    <p>If you are on Windows and don't want Perl and other features of XAMPP, you should go for WAMP. In a similar way, you may use LAMP for Linux and MAMP for Macintosh.</p>
